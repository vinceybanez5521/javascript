// Arithmetic Operators
let x;

x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 7 % 5;

// Concatenation
x = "Hello" + " " + "World";

// Exponent
x = 2 ** 3;

// Increment
x = 1;
// x = x + 1;
x++;

// Decrement
x = 1;
// x = x - 1;
x--;

// Assignment Operators
x = 10;
x += 5; // x = x + 5
x -= 5; // x = x - 5
x *= 5; // x = x * 5
x /= 5; // x = x / 5
// x %= 5; // x = x % 5
x **= 5; // x = x ** 5

// Comparison Operators
x = 2 == 2;
x = 2 == "2";
x = 2 === "2"; // strict - compares both data type and value
x = 2 != "2";
x = 2 !== "2";
x = 2 > 1;
x = 2 >= 1;
x = 2 < 1;
x = 2 <= 1;

